#funciones con retorno
def uno(a,b):
    return(a*b)

def dos(a,b):
    return(a+b)

def tres(a,b):
    return(a-b)

def cuatro(a,b):
    return(a/b)

multiply=uno(5,2)
print("La multiplicación es: ",multiply)
suma=dos(5,2)
print("La suma es:",suma)
resta=tres(5,2)
print("La resta es:",resta)
div=cuatro(5,2)
print("La división es:",div)